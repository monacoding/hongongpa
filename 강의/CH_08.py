#문자열 [숫자 : 숫자]
#예를 들어
# "안녕하세요"[1:4] 라고 하면
# 녕하세 가 출력이 된다.
# 1번째 부터 4번째 전까지 출력이 된다.
# 1은 포함 4는 포함 안된다.
# 음수로 표현하면 
# "안녕하세요"[1:-2] 라고 하면
# 끝 숫자 전까지 출력이 된다는 것을 잊지 말것 

#"안녕하세요" [3:]" 라고 하면
#세요 가 출력이 된다.
#"안녕하세요" [:3]" 라고 하면
#안녕하 가 출력이 된다.

print("안녕하세요"[:])

print("안녕하세요"[1:4:2]) #녕세 1번째 부터 4번째 전까지 2칸씩 띄어서 출력
# 즉 첫번째 숫자는 시작점, 두번째 숫자는 끝점, 세번째 숫자는 몇칸씩 띄어서 출력할 것인가를 의미한다.

