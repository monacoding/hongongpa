#문자열 [시작인덱스:]
#만약 스텝에 -1을 넣으면 역순으로 출력이 된다.
#그 원리는 끝에서 부터 시작해서 처음까지 출력이 된다. 
#예를 들어 "안녕하세요"[::-1] 이라고 하면
#요세하녕안 이 출력이 된다.
#즉 역순으로 출력이 된다는 것이다.
#::-1 에서 -1은 원래는 스텝이기 때문에 간격이지만, 
#-1을 넣으면 역순으로 출력이 된다는 것을 알아두자.